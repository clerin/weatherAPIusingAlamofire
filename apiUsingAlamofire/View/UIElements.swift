import Foundation
import UIKit

extension UITextField {
    func textStyle(){
        self.backgroundColor = UIColor.white
    }
}

extension UILabel{
    
    func labelStyle(){
         self.backgroundColor = UIColor.gray
         self.textColor = UIColor.white
    }
}


